 # Ubah element list
## ubah elemen ke-4 dengan nilai lainnya
bunga = ['mawar', 'melati', 'tulip', 'kamboja', 'levender', 'teratai']
print()
print("List sebelum di ubah :", bunga)
print()
bunga[4] = 'kembang sepatu'
print("List sesudah di ubah :", bunga)
print()

## ubah elemen ke 4 sampai dengan elemen terakhir
bunga[4:] = ["alamanda", "lily"]
print("Ubah element ke-4 hingga akhir :", bunga)
print()
